//Sumar fracciones a/b + c/d
#include <iostream>

using namespace std;

int main()
{
	float a,b,c,d,r;
	
	cout<<"ingresa valor a= ";cin>>a;
	cout<<"ingresa valor b= ";cin>>b;
	cout<<"ingresa valor c= ";cin>>c;
	cout<<"ingresa valor d= ";cin>>d;

	r=(a/b)+(c/d);

	cout<<"La suma de fracciones es= "<<r<<"\n";
	
	return 0;
	
}
